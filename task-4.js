// Написати програму яка буде переводити температуру з цельсія в
//  фаренгейти, або навпаки з фаренгейта в цельсії

let celsius = +prompt("Введите температуру в Цельсиях C", " ");
let result = (celsius * (9 / 5) + 32).toFixed(2);
alert(`Температура в Фаренгейтах = ${result}`);
let farengeit = +prompt("Введите температуру в Фаренгейтах F", " ");
let result1 = ((farengeit - 32) * (5 / 9)).toFixed(2);
alert(`Температура в Цельсиях = ${result1}`);